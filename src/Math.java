public class Math {
    public static int sum (int a, int b){
        return a+b;
    }

    public static double dev(int a, int b){
        return (double) a /b;
    }

    public static int mult (int a, int b){
        return a*b;
    }

    public static double subt (int a, int b){
        return a-b;
    }



}
